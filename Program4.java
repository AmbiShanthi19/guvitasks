public class Program4 {

	public static void main(String[] args) {
		int a=20;
		int b=10;
		System.out.println("value of a " + a);
		System.out.println("value of b " + b);
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swapping");
		System.out.println("Value of a " + a);
		System.out.println("Value of b " + b);

	}

}
